// Declare two variables with string values
let Sent1 = "This is the beginning of the string";
let Sent2 = " and this is the end of the string";

// Concatenate the two string variables and store the result in a new variable
let fullSentence = Sent1 + Sent2;

// Use window.alert() to display the concatenated string in an alert box
window.alert(fullSentence);

// Use document.write() to display the concatenated string on the webpage
document.write(fullSentence);

// Write an expression to calculate a sum, for example
let sum = 5 + 10; // This is the expression
window.alert("The sum of 5 and 10 is: " + sum); // Displaying the result of the expression

// Writing the expression result to the document
document.write("<br>The sum of 5 and 10 is: " + sum);
